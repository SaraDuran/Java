
public class Operacoes {
	
	public static float operacaoSoma(float a, float b){
		return(a+b);
	}
	
	public static float operacaoSubtracao(float a, float b){
		return(a-b);
	}
	
	public static float operacaoMultiplicacao(float a, float b){
		return(a*b);
	}
	
	public static float operacaoDivisaoResto(float a, float b){
		return(a%b);
	}
	
	public static float operacaoDivisao(float a, float b){
		
		return(a/b);
	}

}
